#!/usr/bin/env node
/* =====================================================
   diagnose-face-provider.js

   Identifies employees whose stored face embeddings
   were generated by a DIFFERENT provider than the one
   currently configured (FACE_PROVIDER env var).

   When FACE_PROVIDER=opencv_sface:
     • face_enrollment_version 3 + face_model_provider='opencv_sface'  → OK
     • anything else                                                     → MISMATCH

   Usage:
     cd backend
     node scripts/diagnose-face-provider.js
     node scripts/diagnose-face-provider.js --fix        (mark for re-registration)
     node scripts/diagnose-face-provider.js --dry-run    (preview only, default)
   ===================================================== */

require('dotenv').config({ path: require('path').join(__dirname, '../.env') });

const supabase      = require('../config/supabase');
const { FACE_PROVIDER, isOpenCvSFaceProvider } = require('../services/face-service-client');

const args   = process.argv.slice(2);
const DRY_RUN = !args.includes('--fix');

// ── helpers ──────────────────────────────────────────────────────────────────

function col(str, code) { return `\x1b[${code}m${str}\x1b[0m`; }
const red    = (s) => col(s, 31);
const green  = (s) => col(s, 32);
const yellow = (s) => col(s, 33);
const cyan   = (s) => col(s, 36);
const bold   = (s) => col(s, 1);

function embeddingNorm(embedding) {
    if (!Array.isArray(embedding) || embedding.length === 0) return null;
    const sum = embedding.reduce((acc, v) => acc + v * v, 0);
    return Math.sqrt(sum);
}

function analyzeEmbeddings(faceEmbeddings) {
    if (!Array.isArray(faceEmbeddings) || faceEmbeddings.length === 0) {
        return { count: 0, avgNorm: null, minNorm: null, maxNorm: null };
    }

    const norms = faceEmbeddings
        .map(emb => embeddingNorm(Array.isArray(emb) ? emb : null))
        .filter(n => n !== null);

    if (norms.length === 0) return { count: faceEmbeddings.length, avgNorm: null, minNorm: null, maxNorm: null };

    const avg = norms.reduce((a, b) => a + b, 0) / norms.length;
    return {
        count: faceEmbeddings.length,
        avgNorm: parseFloat(avg.toFixed(4)),
        minNorm: parseFloat(Math.min(...norms).toFixed(4)),
        maxNorm: parseFloat(Math.max(...norms).toFixed(4)),
    };
}

// ── classification ────────────────────────────────────────────────────────────
// SFace (opencv_sface) dan face-api.js (FaceRecognitionNet) keduanya
// menghasilkan L2-normalized embedding → norm ≈ 1.0.
// Jika norm jauh dari 1.0, embedding kemungkinan corrupt/invalid.

function classifyEmployee(row) {
    const {
        id_pegawai,
        nama_lengkap,
        face_enrollment_version,
        face_model_provider,
        face_embeddings,
        status_wajah,
        face_registered_at,
    } = row;

    if (!status_wajah || !face_embeddings || face_embeddings.length === 0) {
        return { id_pegawai, nama_lengkap, status: 'NO_FACE', detail: 'Belum registrasi wajah', face_registered_at, embStats: null };
    }

    const embStats = analyzeEmbeddings(face_embeddings);

    // Mismatch heuristics:
    // 1) face_model_provider beda dari FACE_PROVIDER yang sekarang aktif
    // 2) face_enrollment_version < 3 saat FACE_PROVIDER=opencv_sface
    //    (version 3 = SFace path, version 2 = faceapi path)
    // 3) Norm jauh dari [0.85, 1.15] — bisa jadi corrupt

    const providerMismatch = isOpenCvSFaceProvider()
        ? (face_model_provider !== 'opencv_sface' || (face_enrollment_version || 0) < 3)
        : false;

    const normOutOfRange = embStats.avgNorm !== null &&
        (embStats.avgNorm < 0.8 || embStats.avgNorm > 1.3);

    if (providerMismatch) {
        return {
            id_pegawai, nama_lengkap, status: 'MISMATCH', face_registered_at, embStats,
            detail: `Provider mismatch: disimpan sebagai '${face_model_provider || 'unknown'}' (version ${face_enrollment_version}), aktif='${FACE_PROVIDER}'`,
        };
    }

    if (normOutOfRange) {
        return {
            id_pegawai, nama_lengkap, status: 'SUSPECT_NORM', face_registered_at, embStats,
            detail: `Norm rata-rata embedding = ${embStats.avgNorm} (expected ~1.0) — kemungkinan corrupt`,
        };
    }

    return { id_pegawai, nama_lengkap, status: 'OK', face_registered_at, embStats, detail: `OK (${face_model_provider}, v${face_enrollment_version})` };
}

// ── mark for re-registration ──────────────────────────────────────────────────

async function markForReregistration(ids) {
    if (ids.length === 0) return;

    console.log(bold('\n⏳ Marking employees for re-registration...'));

    const { error } = await supabase
        .from('pegawai')
        .update({
            status_wajah:            false,
            face_embeddings:         [],
            vektor_wajah:            null,
            face_enrollment_version: 1,
            face_model_provider:     null,
            face_registered_at:      null,
            face_quality_summary:    {},
        })
        .in('id_pegawai', ids);

    if (error) {
        console.error(red('❌ Gagal update database:'), error.message);
        process.exit(1);
    }

    // Audit log
    const auditRows = ids.map(id => ({
        id_admin: null,
        aksi:     'RESET_WAJAH',
        detail:   {
            id_pegawai:  id,
            alasan:      'Provider mismatch detected by diagnose-face-provider script',
            old_provider: 'faceapi',
            new_provider: FACE_PROVIDER,
        },
    }));

    const { error: auditErr } = await supabase.from('audit_log').insert(auditRows);
    if (auditErr) {
        console.warn(yellow('⚠️  Audit log gagal disimpan:'), auditErr.message);
    }

    console.log(green(`✅ ${ids.length} pegawai berhasil di-reset. Mereka perlu re-register wajah.`));
}

// ── main ──────────────────────────────────────────────────────────────────────

async function main() {
    console.log(bold('\n╔══════════════════════════════════════════════════════╗'));
    console.log(bold('║       Face Provider Diagnosis Tool                   ║'));
    console.log(bold('╚══════════════════════════════════════════════════════╝'));
    console.log(`  FACE_PROVIDER aktif : ${cyan(FACE_PROVIDER)}`);
    console.log(`  Mode                : ${DRY_RUN ? yellow('DRY-RUN (preview only)') : red('--fix (akan update DB!)')}`);
    console.log('');

    // Fetch all employees
    const { data: employees, error } = await supabase
        .from('pegawai')
        .select('id_pegawai, nama_lengkap, face_enrollment_version, face_model_provider, face_embeddings, status_wajah, face_registered_at, is_active, role')
        .eq('role', 'pegawai')
        .order('nama_lengkap');

    if (error) {
        console.error(red('❌ Gagal fetch data pegawai:'), error.message);
        process.exit(1);
    }

    console.log(`  Total pegawai aktif : ${employees.length}`);
    console.log('');

    const results = employees.map(classifyEmployee);

    const ok        = results.filter(r => r.status === 'OK');
    const noFace    = results.filter(r => r.status === 'NO_FACE');
    const mismatch  = results.filter(r => r.status === 'MISMATCH');
    const suspect   = results.filter(r => r.status === 'SUSPECT_NORM');

    // ── Print results ──

    if (ok.length > 0) {
        console.log(bold(green(`✅ OK (${ok.length})`)));
        ok.forEach(r => {
            console.log(`   ${r.nama_lengkap.padEnd(30)} ${r.detail}  | norm avg: ${r.embStats?.avgNorm ?? '-'}`);
        });
        console.log('');
    }

    if (noFace.length > 0) {
        console.log(bold(yellow(`⬜ Belum registrasi wajah (${noFace.length})`)));
        noFace.forEach(r => console.log(`   ${r.nama_lengkap}`));
        console.log('');
    }

    if (suspect.length > 0) {
        console.log(bold(yellow(`⚠️  Embedding norm mencurigakan (${suspect.length})`)));
        suspect.forEach(r => {
            console.log(`   ${r.nama_lengkap.padEnd(30)} ${r.detail}`);
            console.log(`   ${''.padEnd(30)} norm: avg=${r.embStats?.avgNorm} min=${r.embStats?.minNorm} max=${r.embStats?.maxNorm}`);
        });
        console.log('');
    }

    if (mismatch.length > 0) {
        console.log(bold(red(`❌ PROVIDER MISMATCH — harus re-register (${mismatch.length})`)));
        mismatch.forEach(r => {
            const registered = r.face_registered_at
                ? new Date(r.face_registered_at).toLocaleDateString('id-ID', { day: '2-digit', month: 'short', year: 'numeric' })
                : 'unknown';
            console.log(`   ${r.nama_lengkap.padEnd(30)} registrasi: ${registered}`);
            console.log(`   ${''.padEnd(30)} ${yellow(r.detail)}`);
            console.log(`   ${''.padEnd(30)} norm: avg=${r.embStats?.avgNorm} min=${r.embStats?.minNorm} max=${r.embStats?.maxNorm}`);
        });
        console.log('');
    }

    // ── Summary ──

    console.log(bold('── Summary ──────────────────────────────────────────────'));
    console.log(`  ${green('OK')}              : ${ok.length}`);
    console.log(`  ${yellow('Belum registrasi')}: ${noFace.length}`);
    console.log(`  ${yellow('Suspect norm')}    : ${suspect.length}`);
    console.log(`  ${red('Provider mismatch')}: ${mismatch.length}`);
    console.log('');

    if (mismatch.length === 0 && suspect.length === 0) {
        console.log(green('🎉 Tidak ada masalah provider mismatch ditemukan.'));
        return;
    }

    if (DRY_RUN) {
        if (mismatch.length > 0) {
            console.log(yellow('ℹ️  Jalankan dengan --fix untuk reset embedding pegawai bermasalah:'));
            console.log(cyan('   node scripts/diagnose-face-provider.js --fix'));
            console.log('');
            console.log(yellow('   Pegawai yang di-reset harus melakukan registrasi wajah ulang'));
            console.log(yellow('   di halaman /register-face setelah FACE_PROVIDER=opencv_sface aktif.'));
        }
    } else {
        const idsToFix = [
            ...mismatch.map(r => r.id_pegawai),
            ...suspect.map(r => r.id_pegawai),
        ];
        await markForReregistration(idsToFix);

        console.log('');
        console.log(bold('📋 Langkah selanjutnya:'));
        console.log('  1. Pastikan FACE_PROVIDER=opencv_sface di file .env backend');
        console.log('  2. Beritahu pegawai berikut untuk registrasi ulang wajah:');
        [...mismatch, ...suspect].forEach(r => console.log(`     - ${r.nama_lengkap}`));
        console.log('  3. Pegawai buka halaman: /pegawai/register-face.html');
        console.log('  4. Jalankan script ini lagi untuk konfirmasi semua sudah OK');
    }
}

main().catch(err => {
    console.error(red('Fatal error:'), err.message);
    process.exit(1);
});
